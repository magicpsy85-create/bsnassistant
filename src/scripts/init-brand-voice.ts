async function init() {
  const docRef = firestore.collection('brand_voice').doc('main');
  const existing = await docRef.get();

  if (existing.exists) {
    console.error('[init-brand-voice] doc already exists. Use admin UI (Phase A-5) to update.');
    process.exit(1);
  }

  await docRef.set({
    principles: PRINCIPLES,
    companyInfo: COMPANY_INFO,
    cardRules: CARD_RULES,
    version: 1,
    updatedAt: new Date().toISOString(),
    updatedBy: 'init-script',
  });

  console.log('[init-brand-voice] brand_voice/main created (version=1)');
  console.log(`  - principles: ${PRINCIPLES.length} chars`);
  console.log(`  - companyInfo: ${COMPANY_INFO.length} chars`);
  console.log(`  - cardRules: ${CARD_RULES.length} chars`);
  process.exit(0);
}

init().catch(err => {
  console.error('[init-brand-voice] failed:', err);
  process.exit(1);
});
